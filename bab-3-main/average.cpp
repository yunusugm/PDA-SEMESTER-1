#include <iostream>

using namespace std;

int main()
{
    // Buat Variabel
    int numOne = 8, numTwo = 11, numThree = 27, numFour = 14, numFive = 7;
    float average;

    // Operasi Matematika
    average = float(numOne + numTwo + numThree + numFour + numFive) / 5;

    // Output
    cout << "Rata-rata dari 5 bilangan tersebut = "  << average;
    return 0;
}


